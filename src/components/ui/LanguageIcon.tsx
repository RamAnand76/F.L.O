export function LanguageIcon({ language, className = "w-4 h-4" }: LanguageIconProps) {
  if (!language) return null;

  // Case-insensitive lookup
  const normalizedLang = Object.keys(languageConfig).find(
    key => key.toLowerCase() === language.toLowerCase()
  );
  
  const config = (normalizedLang ? languageConfig[normalizedLang] : null) || {
    color: '#71717a',
    svg: <Code2 className="w-full h-full opacity-50" />
  };

  return (
    <div className={className} style={{ color: config.color }}>
      {config.svg}
    </div>
  );
}
